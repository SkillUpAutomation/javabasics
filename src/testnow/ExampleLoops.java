package testnow;

public class ExampleLoops {

    public static void main(String s[]){
         //For Loop
        for (int i=0; i<=5;i++){
            System.out.println("For Loop "+i);
        }

        //While loop
        int x=0;
        while(x<5){
            System.out.println("While loop " +x);
            x++;
        }

        //Do While Loop
        int y=0;
        do{
            System.out.println("Do While " +y);
            y++;
        }while (y<5);


    }
}
